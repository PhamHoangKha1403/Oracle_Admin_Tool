DROP TABLE DON_THUOC CASCADE CONSTRAINTS;
DROP TABLE HSBA_DV CASCADE CONSTRAINTS;
DROP TABLE HSBA CASCADE CONSTRAINTS;
DROP TABLE BENH_NHAN CASCADE CONSTRAINTS;
DROP TABLE THONG_BAO CASCADE CONSTRAINTS;
DROP TABLE NHAN_VIEN CASCADE CONSTRAINTS;
DROP TABLE KHOA CASCADE CONSTRAINTS;

CREATE TABLE KHOA (
    MA_KHOA VARCHAR2(10) PRIMARY KEY,
    TEN_KHOA VARCHAR2(100) NOT NULL
);

CREATE TABLE NHAN_VIEN (
    MA_NV VARCHAR2(10) PRIMARY KEY,
    HO_TEN VARCHAR2(100) NOT NULL,
    PHAI VARCHAR2(5),
    NGAY_SINH DATE,
    CCCD VARCHAR2(12) UNIQUE,
    QUE_QUAN VARCHAR2(200),
    SDT VARCHAR2(15),
    VAI_TRO VARCHAR2(30),
    CHUYEN_KHOA VARCHAR2(10),

    CONSTRAINT CK_PHAI
        CHECK (PHAI IN ('Nam','Nữ')),

    CONSTRAINT CK_VAITRO
        CHECK (VAI_TRO IN ('Bác sĩ','Kỹ thuật viên','Điều phối viên','Bệnh nhân')),

    CONSTRAINT FK_NV_KHOA
        FOREIGN KEY (CHUYEN_KHOA)
        REFERENCES KHOA(MA_KHOA)
);

CREATE TABLE BENH_NHAN (
    MA_BN VARCHAR2(10) PRIMARY KEY,
    SO_NHA VARCHAR2(50),
    TEN_DUONG VARCHAR2(100),
    QUAN_HUYEN VARCHAR2(100),
    TINH_TP VARCHAR2(100),
    TIEN_SU_BENH VARCHAR2(500),
    TIEN_SU_BENH_GD VARCHAR2(500),
    DI_UNG_THUOC VARCHAR2(500),

    CONSTRAINT FK_BN_NV
        FOREIGN KEY (MA_BN)
        REFERENCES NHAN_VIEN(MA_NV)
);

CREATE TABLE HSBA (
    MA_HSBA VARCHAR2(10) PRIMARY KEY,
    MA_BN VARCHAR2(10) NOT NULL,
    NGAY DATE,
    CHAN_DOAN VARCHAR2(500),
    DIEU_TRI VARCHAR2(500),
    MA_BS VARCHAR2(10) NOT NULL,
    MA_KHOA VARCHAR2(10) NOT NULL,
    KET_LUAN VARCHAR2(500),

    CONSTRAINT FK_HSBA_BN
        FOREIGN KEY (MA_BN)
        REFERENCES BENH_NHAN(MA_BN),

    CONSTRAINT FK_HSBA_BS
        FOREIGN KEY (MA_BS)
        REFERENCES NHAN_VIEN(MA_NV),

    CONSTRAINT FK_HSBA_KHOA
        FOREIGN KEY (MA_KHOA)
        REFERENCES KHOA(MA_KHOA)
);

CREATE TABLE DON_THUOC (
    MA_HSBA VARCHAR2(10),
    NGAY_DT DATE  NOT NULL,
    TEN_THUOC VARCHAR2(200) NOT NULL,
    LIEU_DUNG VARCHAR2(200),

    CONSTRAINT PK_DON_THUOC
        PRIMARY KEY (MA_HSBA, NGAY_DT, TEN_THUOC),

    CONSTRAINT FK_DT_HSBA
        FOREIGN KEY (MA_HSBA)
        REFERENCES HSBA(MA_HSBA)
);


CREATE TABLE HSBA_DV (
    MA_HSBA VARCHAR2(10),
    LOAI_DV VARCHAR2(100),
    NGAY_DV DATE,
    MA_KTV VARCHAR2(10) NOT NULL,
    KET_QUA VARCHAR2(500),

    CONSTRAINT PK_HSBA_DV
        PRIMARY KEY (MA_HSBA, LOAI_DV, NGAY_DV),

    CONSTRAINT FK_DV_HSBA
        FOREIGN KEY (MA_HSBA)
        REFERENCES HSBA(MA_HSBA),

    CONSTRAINT FK_DV_KTV
        FOREIGN KEY (MA_KTV)
        REFERENCES NHAN_VIEN(MA_NV)
);

CREATE TABLE THONG_BAO (
    MA_TB VARCHAR2(10) PRIMARY KEY,
    NOI_DUNG VARCHAR2(500) NOT NULL,
    NGAY_GIO DATE NOT NULL,
    DIA_DIEM VARCHAR2(200) NOT NULL,
    MA_NV VARCHAR2(10),
    SECURITY_LABEL NUMBER,

    CONSTRAINT FK_TB_NV
        FOREIGN KEY (MA_NV)
        REFERENCES NHAN_VIEN(MA_NV)
);

CREATE OR REPLACE TRIGGER TRG_CHECK_BENHNHAN
BEFORE INSERT OR UPDATE ON BENH_NHAN
FOR EACH ROW
DECLARE
    v_role NHAN_VIEN.VAI_TRO%TYPE;
BEGIN
    BEGIN
        SELECT VAI_TRO
        INTO v_role
        FROM NHAN_VIEN
        WHERE MA_NV = :NEW.MA_BN;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20010,
            'MA_NV khong ton tai');
    END;

    IF v_role <> 'Bệnh nhân' THEN
        RAISE_APPLICATION_ERROR(-20001,
        'MA_NV phai co vai tro Benh nhan');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_CHECK_BACSI
BEFORE INSERT OR UPDATE ON HSBA
FOR EACH ROW
DECLARE
    v_role NHAN_VIEN.VAI_TRO%TYPE;
BEGIN
    BEGIN
        SELECT VAI_TRO
        INTO v_role
        FROM NHAN_VIEN
        WHERE MA_NV = :NEW.MA_BS;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20010,
            'MA_NV khong ton tai');
    END;
    
    IF v_role <> 'Bác sĩ' THEN
        RAISE_APPLICATION_ERROR(-20002,
        'MA_BS phai la Bac si');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_CHECK_KTV
BEFORE INSERT OR UPDATE ON HSBA_DV
FOR EACH ROW
DECLARE
    v_role NHAN_VIEN.VAI_TRO%TYPE;
BEGIN
    BEGIN
        SELECT VAI_TRO
        INTO v_role
        FROM NHAN_VIEN
        WHERE MA_NV = :NEW.MA_KTV;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20010,
            'MA_NV khong ton tai');
    END;

    IF v_role <> 'Kỹ thuật viên' THEN
        RAISE_APPLICATION_ERROR(-20003,
        'MA_KTV phai la Ky thuat vien');
    END IF;
END;
/
COMMIT;